import { mkdir, readFile, writeFile, access } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const seedPath = path.join(repoRoot, 'src', 'domains', 'buyer-hub', 'data', 'nationalCarMartInventory.seed.csv')
const publicDir = path.join(repoRoot, 'public', 'inventory', 'national-car-mart')
const generatedTsPath = path.join(repoRoot, 'src', 'domains', 'buyer-hub', 'data', 'nationalCarMartInventory.generated.ts')
const placeholderUrl = 'https://cdn-ds.com/noimage/w_1920/noimage.jpg'
const placeholderFileName = 'placeholder.jpg'

function escapeTsString(value) {
  return String(value ?? '').replace(/\\/g, '\\\\').replace(/`/g, '\\`')
}

async function fileExists(targetPath) {
  try {
    await access(targetPath)
    return true
  } catch {
    return false
  }
}

async function downloadToFile(url, targetPath) {
  const response = await fetch(url)
  if (!response.ok) throw new Error(`Failed to download ${url}: ${response.status}`)
  const buffer = Buffer.from(await response.arrayBuffer())
  await writeFile(targetPath, buffer)
}

function parseCsv(text) {
  const lines = text.trim().split(/\r?\n/)
  const headers = lines.shift().split(',')
  return lines.map((line) => {
    const values = []
    let current = ''
    let inQuotes = false
    for (let i = 0; i < line.length; i += 1) {
      const char = line[i]
      const next = line[i + 1]
      if (char === '"') {
        if (inQuotes && next === '"') {
          current += '"'
          i += 1
        } else {
          inQuotes = !inQuotes
        }
      } else if (char === ',' && !inQuotes) {
        values.push(current)
        current = ''
      } else {
        current += char
      }
    }
    values.push(current)
    return Object.fromEntries(headers.map((header, index) => [header, values[index] ?? '']))
  })
}

function buildGeneratedModule(units) {
  const header = `/* eslint-disable */\n// AUTO-GENERATED by scripts/seed-national-car-mart-inventory.mjs\n\nexport interface PublicInventoryUnit {\n  id: string\n  listingId: number\n  vin: string\n  stockNumber: string\n  year: number\n  make: string\n  model: string\n  trim: string\n  bodyStyle: string\n  mileage: number\n  askingPrice: number\n  status: string\n  daysInStock: number\n  transmission: string\n  imageUrl: string\n  detailUrl: string\n  photoArchiveStatus: string\n  highlights: string[]\n  available: boolean\n}\n\nexport const NATIONAL_CAR_MART_INVENTORY: PublicInventoryUnit[] = [\n`
  const rows = units.map((unit) => {
    const highlights = unit.highlights.map((h) => `\`${escapeTsString(h)}\``).join(', ')
    return `  {\n    id: \`${escapeTsString(unit.id)}\`,\n    listingId: ${unit.listingId},\n    vin: \`${escapeTsString(unit.vin)}\`,\n    stockNumber: \`${escapeTsString(unit.stockNumber)}\`,\n    year: ${unit.year},\n    make: \`${escapeTsString(unit.make)}\`,\n    model: \`${escapeTsString(unit.model)}\`,\n    trim: \`${escapeTsString(unit.trim)}\`,\n    bodyStyle: \`${escapeTsString(unit.bodyStyle)}\`,\n    mileage: ${unit.mileage},\n    askingPrice: ${unit.askingPrice},\n    status: \`frontline\`,\n    daysInStock: 0,\n    transmission: \`${escapeTsString(unit.transmission)}\`,\n    imageUrl: \`${escapeTsString(unit.imagePath)}\`,\n    detailUrl: \`${escapeTsString(unit.detailUrl)}\`,\n    photoArchiveStatus: \`${escapeTsString(unit.photoArchiveStatus)}\`,\n    highlights: [${highlights}],\n    available: true,\n  }`
  }).join(',\n')
  return `${header}${rows}\n]\n`
}

async function main() {
  const csvRaw = await readFile(seedPath, 'utf-8')
  const seed = parseCsv(csvRaw).map((row) => ({
    ...row,
    listingId: Number(row.listingId),
    year: Number(row.year),
    mileage: Number(row.mileage),
    askingPrice: Number(row.askingPrice),
    highlights: row.highlights ? row.highlights.split(' | ').map((part) => part.trim()).filter(Boolean) : [],
  }))

  await mkdir(publicDir, { recursive: true })
  const placeholderPath = path.join(publicDir, placeholderFileName)
  if (!(await fileExists(placeholderPath))) await downloadToFile(placeholderUrl, placeholderPath)

  for (const unit of seed) {
    const desiredFileName = path.basename(unit.imagePath || '')
    if (!desiredFileName || desiredFileName === placeholderFileName || !unit.imageSourceUrl || unit.photoArchiveStatus.includes('unresolved') || unit.imageSourceUrl === placeholderUrl) {
      unit.imagePath = `/inventory/national-car-mart/${placeholderFileName}`
      continue
    }
    const targetPath = path.join(publicDir, desiredFileName)
    if (!(await fileExists(targetPath))) {
      try {
        await downloadToFile(unit.imageSourceUrl, targetPath)
      } catch {
        unit.imagePath = `/inventory/national-car-mart/${placeholderFileName}`
        continue
      }
    }
    unit.imagePath = `/inventory/national-car-mart/${desiredFileName}`
  }

  await writeFile(generatedTsPath, buildGeneratedModule(seed), 'utf-8')
  console.log(`Generated buyer hub inventory for ${seed.length} units.`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
